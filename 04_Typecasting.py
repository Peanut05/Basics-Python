# Typecasting :-
'''Typecasting is used for convert one data type to another when it's possible 
or define a type of data before the variable 
there are many functions for typecasting of data
that is 1) str(32) => "32" it's a string
        2) int("32") => 32 it's a integer 
        2) Floata(32) => 32.0 it's a float '''

a = "32"
print(a,type(a)) # Now it's a string   
a = int(a)   
print(a,type(a)) # Now it's a intiger
a = float(a)
print(a,type(a)) # Now it's a float
a = str(a)
print(a,type(a)) # Now it's also become a string
